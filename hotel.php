Bangladesh has a great heritage of food. In the capital city Dhaka, 
there are some very special food which are cheap, but very rich in taste, 
and only known to the food-lovers of the city. Here is a list of 5 very special
 dishes of Dhaka which you won’t want to miss, if you have some time in hand and 
 want to experience the food heritage of the city.Best morog-polao at Bangladesh.
 Morog-polao is a very special food of Bangladesh, specially served at marriage 
 ceremonies or any other big family festivals. “Morog” is chicken, and “polao” 
 is rice cooked in a special way. Morog-polao is specially cooked rice with large
 portion of chicken, served with a special drink named “borhani”. A very testy and
 famous food of Bangladesh.Vuna khichuri is another famous food of Bangladesh. 
 It is made of rice, dal, and meat – chicken or beef. Rice is cooked dry with dal
 in a special way, and meat is added with it. This is a very delicious food of Bangladesh.
 You’ll find the best Vuna Khichuri in Dhaka at Ghoroa Restaurent.
   
